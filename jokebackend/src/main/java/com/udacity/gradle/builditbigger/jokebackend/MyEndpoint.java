package com.udacity.gradle.builditbigger.jokebackend;

import com.google.api.server.spi.config.Api;
import com.google.api.server.spi.config.ApiMethod;
import com.google.api.server.spi.config.ApiNamespace;

import javax.inject.Named;

import com.udacity.gradle.javajokes.Joker;

/**
 * An endpoint class we are exposing
 */
@Api(
        name = "myApi",
        version = "v1",
        namespace = @ApiNamespace(
                ownerDomain = "jokebackend.builditbigger.gradle.udacity.com",
                ownerName = "jokebackend.builditbigger.gradle.udacity.com",
                packagePath = ""
        )
)
public class MyEndpoint {

    @ApiMethod(name = "tellJoke")
    public MyBean tellJoke() {
        Joker myJoke = new Joker();
//        myData = myJoke.getJoke();
        MyBean response = new MyBean();
        response.setData(myJoke.getJoke());

        return response;
    }


}
